 S IBA(365.12,"+1,",.02)=IBNID
 S IBA(365.12,"+1,",.05)=$P(IB1,U,3)
 S IBA(365.12,"+1,",.06)=$P(IB1,U,4)
 S IBA(365.121,"+2,+1,",.01)=IBIIV
 S IBA(365.121,"+2,+1,",.02)=IBACT
 S IBA(365.121,"+2,+1,",.08)=IBID
 S IBA(365.121,"+2,+1,",.1)=0
 S IBA(365.121,"+2,+1,",.13)=$$NOW^XLFDT()
 S IBA(365.121,"+2,+1,",.14)=$P(IB1,U,7)
 S IBA(365.121,"+2,+1,",.15)=$P(IB1,U,8)
 S IBA(365.121,"+2,+1,",.07)=IBAUTO
 D UPDATE^DIE("","IBA","","IBERRM") ; file new PAYER entry
 I $D(IBERRM) D  Q
 . D BMES^XPDUTL("******Unable to add PAYER : "_$P(IB1,U))
 S IBCNT=IBCNT+1
 D BMES^XPDUTL("PAYER "_$P(IB1,U)_" ADDED WITH A NATIONAL ID OF: "_IBNID)
 Q
