 Q
UPDATE ;CHECK AND ADD MISSING RECORDS
 Q:IBNOAP 
 S IBNID=$P(IB1,U,2)
 ;QUIT IF AN ENTRY ALREADY EXIST FOR THAT NATIONAL ID
 Q:+$O(^IBE(365.12,"C",IBNID,""))
 S IBACT=$S($P(IB1,U,5)="Y":1,1:0)
 S IBAUTO=$S($P(IB1,U,9)="Y":1,1:0)
 S IBID=$S($P(IB1,U,6)="Y":1,1:0)
 S IBIIV=$O(^IBE(365.13,"B","IIV",0))
 I 'IBIIV D
 .N DIC,X,Y
 .S DIC="^IBE(365.13,",X="IIV",DIC(0)=""
 .K DD,DO
 .D FILE^DICN
 .K DO
 .S IBIIV=+Y
 I 'IBIIV D  Q
 .S IBNOAP=1
 .D BMES^XPDUTL("Unable to add IIV to the PAYER APPLICATION file!")
